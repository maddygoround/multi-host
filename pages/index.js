import React, { useState, useEffect } from "react";
import Header from "../components/header/Header";
import { Button, Col, Form, Input, message, Row } from "antd";
import Head from "next/head";
import axios from "axios";

require("./index.less");

export default function Home() {
  const [domainUrl, setDomainUrl] = useState("");
  const [address, setAddress] = useState(null);
  const [isCheckingHost, setIsCheckingHost] = useState(false);
  const [form] = Form.useForm();

  const addDomainhandler = (domain_url) => {
    const body = {
      domain_url: domain_url,
    };
    setIsCheckingHost(true);
    const url = `/api/checkhost`;
    axios
      .post(url, body)
      .then((response) => {
        console.log(response.data);
        if (response.data) {
          setAddress(response.data.name);
        } else {
          message.error("Domain doesn't exist");
        }
        setIsCheckingHost(false);
      })
      .catch((error) => {
        console.log(error);
        setIsCheckingHost(false);
      });
  };

  const updateConfig = (domainurl) => {
    const url = `/api/getConfig`;
    axios
      .get(domainurl)
      .then((response) => {
        console.log(response);
      })
      .catch((error) => {
        console.log(error);
      });
  };
  useEffect(() => {
    if (domainUrl) {
      addDomainhandler(domainUrl);
    }
  }, [domainUrl]);
  return (
    <div className="container-fluid">
      <Head>
        <title>Multi-Host</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <Row className="main">
        <Col className="description-section" xl={10} md={24} xs={24}>
          <div>
            <h2>
              <span className="hightlight-text">Deploy App Servers</span>
              <br />
              Close to Your Users
            </h2>
            <p class="text-lead my-2">
              Run your full stack apps (and databases!) all over the world. No
              ops required.
            </p>
          </div>
        </Col>

        <Col className="relative demo-section"  xl={14} md={24} xs={24}>
          <div className="playarea bg-gray-700/30 border border-violet-200/10 w-full rounded-2xl xl:text-lg">
            <Form form={form}>
              <Row className="">
                <Col span={24}>
                  <Form.Item name="domain_url" rules={[{ required: true }]}>
                    <Input
                      onChange={(e) => setDomainUrl(e.target.value)}
                      placeholder="Enter site-url, that you wanna host on your domain"
                    />
                  </Form.Item>
                </Col>
                <Col span={24} className="d-flex justify-content-center">
                  <Button
                    type="primary"
                    danger
                    disabled={!address}
                    className=" d-flex align-items-center justify-content-center"
                    size="large"
                    style={{ color: "black" }}
                    onClick={() => updateConfig(domainUrl)}
                  >
                    Deploy &rarr;
                  </Button>
                </Col>
                {!isCheckingHost && address && (
                  <div
                    className="h6 text-success text-center"
                    style={{ width: "100%" }}
                  >
                    Address is :
                    <mark className="text-lead text-underline">{address}</mark>
                  </div>
                )}
              </Row>
            </Form>
          </div>
        </Col>

        {/*<div className="grid">
          <Button
            disabled={!address}
            className="card d-flex align-items-center justify-content-center"
            size="large"
            style={{ color: "black" }}
            onClick={() => updateConfig(domainUrl)}
          >
            Deploy &rarr;
          </Button>
              </div>*/}
      </Row>
    </div>
  );
}
